const projectState = {
  lexicaz: 0,
  mors: 0
};

function getCurrentLanguage() {
  return localStorage.getItem("siteLanguage") || "en";
}

function translateElementAttributes(element, language) {
  Object.entries(element.dataset).forEach(([datasetKey, translationKey]) => {
    if (!datasetKey.startsWith("i18n") || datasetKey === "i18n") {
      return;
    }

    const attributeName = datasetKey
      .replace("i18n", "")
      .replace(/^[A-Z]/, (letter) => letter.toLowerCase())
      .replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`);

    const translation = translations[language][translationKey];

    if (translation) {
      element.setAttribute(attributeName, translation);
    }
  });
}

function setLanguage(language) {
  const selectedLanguage = translations[language] ? language : "en";
  const dictionary = translations[selectedLanguage];

  document.documentElement.lang = selectedLanguage;
  document.title = dictionary.metaTitle;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const translation = dictionary[key];

    if (translation) {
      element.textContent = translation;
    }
  });

  document
    .querySelectorAll("[data-i18n-aria-label], [data-i18n-alt], [data-i18n-title], [data-i18n-href], [data-i18n-data-screenshot-hint]")
    .forEach((element) => {
      translateElementAttributes(element, selectedLanguage);
    });

  document.querySelectorAll(".language-button").forEach((button) => {
    const isActive = button.dataset.lang === selectedLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  const navbar = document.querySelector(".navbar");
  const toggleButton = document.querySelector(".navbar-toggle");

  if (navbar && toggleButton) {
    const isHidden = navbar.classList.contains("navbar-hidden");
    toggleButton.setAttribute("aria-label", isHidden ? dictionary["aria.showNav"] : dictionary["aria.hideNav"]);
  }

  document.querySelectorAll(".flip-card[data-project]").forEach((card) => {
    updateProjectScreenshot(card, false);
  });

  localStorage.setItem("siteLanguage", selectedLanguage);
}

function getCaption(image) {
  const language = getCurrentLanguage();
  return image.captions[language] || image.captions.en;
}

function updateProjectScreenshot(card, animate = true) {
  const projectName = card.dataset.project;
  const gallery = projectGalleries[projectName];

  if (!gallery) {
    return;
  }

  const currentIndex = projectState[projectName];
  const currentImage = gallery[currentIndex];

  const screenshot = card.querySelector(".project-screenshot");
  const caption = card.querySelector(".screenshot-caption");
  const counter = card.querySelector(".screenshot-counter");

  if (!screenshot || !caption || !counter) {
    return;
  }

  const applyImage = () => {
    const translatedCaption = getCaption(currentImage);
    screenshot.src = currentImage.src;
    screenshot.alt = translatedCaption;
    caption.textContent = translatedCaption;
    counter.textContent = `${currentIndex + 1} / ${gallery.length}`;

    if (animate) {
      screenshot.classList.add("image-slide-animation");
    }
  };

  screenshot.classList.remove("image-slide-animation");

  if (animate) {
    setTimeout(applyImage, 80);
  } else {
    applyImage();
  }
}

function showNextScreenshot(card) {
  const projectName = card.dataset.project;
  const gallery = projectGalleries[projectName];
  const currentIndex = projectState[projectName];
  const isLastScreenshot = currentIndex === gallery.length - 1;

  if (isLastScreenshot) {
    returnToProjectInfo(card);
    projectState[projectName] = 0;
    return;
  }

  projectState[projectName] = currentIndex + 1;
  updateProjectScreenshot(card);
}

function flipProjectCard(card) {
  const projectName = card.dataset.project;

  projectState[projectName] = 0;
  updateProjectScreenshot(card);
  card.classList.add("is-flipped");
}

function returnToProjectInfo(card) {
  card.classList.remove("is-flipped");
}

function setupFlipCards() {
  const cards = document.querySelectorAll(".flip-card[data-project]");

  cards.forEach((card) => {
    const logoButton = card.querySelector(".project-logo-button");
    const screenshotFrame = card.querySelector(".screenshot-frame");
    const backButton = card.querySelector(".back-to-info");

    if (!logoButton || !screenshotFrame || !backButton) {
      return;
    }

    logoButton.addEventListener("click", () => {
      flipProjectCard(card);
    });

    screenshotFrame.addEventListener("click", () => {
      showNextScreenshot(card);
    });

    screenshotFrame.addEventListener("keydown", (event) => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        showNextScreenshot(card);
      }
    });

    backButton.addEventListener("click", () => {
      returnToProjectInfo(card);
      projectState[card.dataset.project] = 0;
    });
  });
}

function setupNavbarToggle() {
  const navbar = document.querySelector(".navbar");
  const toggleButton = document.querySelector(".navbar-toggle");

  if (!navbar || !toggleButton) {
    return;
  }

  toggleButton.addEventListener("click", () => {
    const language = getCurrentLanguage();
    const dictionary = translations[language];
    const isHidden = navbar.classList.toggle("navbar-hidden");

    toggleButton.textContent = isHidden ? "˅" : "˄";
    toggleButton.setAttribute("aria-expanded", String(!isHidden));
    toggleButton.setAttribute(
      "aria-label",
      isHidden ? dictionary["aria.showNav"] : dictionary["aria.hideNav"]
    );
  });
}

function setupLanguageSwitcher() {
  const languageButtons = document.querySelectorAll(".language-button[data-lang]");

  languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.dataset.lang);
    });
  });
}

setupFlipCards();
